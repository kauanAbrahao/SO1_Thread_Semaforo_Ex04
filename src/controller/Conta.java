package controller;

public class Conta {
	private int codigo;
	private int valor;
	private int saldo;
	
	public int getConta() {
		return codigo;
	}
	public void setConta(int conta) {
		this.codigo = conta;
	}
	public int getValor() {
		return valor;
	}
	public void setValor(int valor) {
		this.valor = valor;
	}
	public int getSaldo() {
		return saldo;
	}
	public void setSaldo(int saldo) {
		this.saldo = saldo;
	}
}
